package com.ewgvip.buyer.android.models;

/**
 * 优惠券详细信息的实体类封装对象
 * Created by Administrator on 2015/10/12.
 */
public class CouponInformation {

    private String coupon_id;//优惠券id
    private String coupon_pic;//展示图片URL
    private String coupon_name;//优惠券名称
    private String coupon_begin_time;//优惠券开始时间
    private String coupon_end_time;//优惠券开始时间
    private String coupon_amount;//优惠券金额
    private String coupon_order_amount;//可用订单金额
    private String coupon_surplus_amount;//剩余数量
    private String coupon_status;//是否可领取

    public CouponInformation() {
        super();
    }

    public CouponInformation(String coupon_id, String coupon_pic, String coupon_name, String coupon_begin_time, String coupon_end_time, String coupon_amount, String coupon_order_amount, String coupon_surplus_amount, String coupon_status) {
        this.coupon_id = coupon_id;
        this.coupon_pic = coupon_pic;
        this.coupon_name = coupon_name;
        this.coupon_begin_time = coupon_begin_time;
        this.coupon_end_time = coupon_end_time;
        this.coupon_amount = coupon_amount;
        this.coupon_order_amount = coupon_order_amount;
        this.coupon_surplus_amount = coupon_surplus_amount;
        this.coupon_status = coupon_status;
    }

    public String getCoupon_status() {
        return coupon_status;
    }

    public String getCoupon_surplus_amount() {
        return coupon_surplus_amount;
    }

    public String getCoupon_amount() {
        return coupon_amount;
    }

    public String getCoupon_order_amount() {
        return coupon_order_amount;
    }

    public String getCoupon_id() {
        return coupon_id;
    }

    public String getCoupon_pic() {
        return coupon_pic;
    }

    public String getCoupon_name() {
        return coupon_name;
    }

    public String getCoupon_begin_time() {
        return coupon_begin_time;
    }

    public String getCoupon_end_time() {
        return coupon_end_time;
    }

    public void setCoupon_id(String coupon_id) {
        this.coupon_id = coupon_id;
    }

    public void setCoupon_pic(String coupon_pic) {
        this.coupon_pic = coupon_pic;
    }

    public void setCoupon_name(String coupon_name) {
        this.coupon_name = coupon_name;
    }

    public void setCoupon_begin_time(String coupon_begin_time) {
        this.coupon_begin_time = coupon_begin_time;
    }

    public void setCoupon_end_time(String coupon_end_time) {
        this.coupon_end_time = coupon_end_time;
    }

    public void setCoupon_amount(String coupon_amount) {
        this.coupon_amount = coupon_amount;
    }

    public void setCoupon_order_amount(String coupon_order_amount) {
        this.coupon_order_amount = coupon_order_amount;
    }

    public void setCoupon_surplus_amount(String coupon_surplus_amount) {
        this.coupon_surplus_amount = coupon_surplus_amount;
    }

    public void setCoupon_status(String coupon_status) {
        this.coupon_status = coupon_status;
    }

    @Override
    public String toString() {
        return "CouponInformation{" +
                "coupon_id='" + coupon_id + '\'' +
                ", coupon_pic='" + coupon_pic + '\'' +
                ", coupon_name='" + coupon_name + '\'' +
                ", coupon_begin_time='" + coupon_begin_time + '\'' +
                ", coupon_end_time='" + coupon_end_time + '\'' +
                ", coupon_amount='" + coupon_amount + '\'' +
                ", coupon_order_amount='" + coupon_order_amount + '\'' +
                ", coupon_surplus_amount='" + coupon_surplus_amount + '\'' +
                ", coupon_status='" + coupon_status + '\'' +
                '}';
    }
}